def NumericalOperations():
    print("Sum of two float numbers: 9.5 + 10.5 =", 9.5 + 10.5)
    print("type(20.0):", type(20.0))
    print("Difference between two integers: 9 + 10 =", 9 + 10)
    print("type(19):", type(19))
    print("Product of a float and integer: 5.5 * 7 =", 5.5 * 7)
    print("type(38.5):", type(38.5))

def main():
    NumericalOperations()
    
if __name__=="__main__":
    main()